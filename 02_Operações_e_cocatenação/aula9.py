adicao = 10 + 10
print('Adição', adicao)

subtracao = 10 - 5
print('Subtração', subtracao)

Multiplicacao = 10 * 10
print('Multiplicacão', Multiplicacao)

divisao = 10 / 2.2  # float
print('Divisão', divisao)

divisao_inteira = 10 // 2.2  # int
print('Divisão inteira', divisao_inteira)

exponenciacao = 2 ** 10  # potenciação
print('Exponenciação', exponenciacao)

modulo = 25 % 2  # resto da divisão
print('Módulo', modulo)

divisivel = modulo == 0
print('É divisível?', divisivel)