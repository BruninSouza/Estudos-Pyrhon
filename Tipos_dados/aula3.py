'''
Pytohn = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
'''

# Aspas simpes
print('Bruno Souza')
print(1, 'Bruno "Souza"')

# Aspas duplas ""
print("Bruno Souza")
print(2, "Bruno 'Souza'")

# Escape
print("Bruno \"Souza\"")

# r
print(r"Bruno \"Souza\"")
