# Tipo de int e float

# int -> Número inteiro
# O tipo int representa qualquer número
# positivo ou negativo. int sem sinal é considerado 
# positivo
# print(11) # int
# print(-11) # int
# print(0) # int

# float -> Número com ponto flutuante
# O tipo float representa qualquer número
# positivo ou negativo com ponto flutuante
# float sem sinal é consideredado positivo
# print(1.1, 10.11) # float
# print(0.0, -1.5) # float 

# A função type mostra o tipo que o Python
# inferiu ao valor
print(type('Otávio'))
print(type(11))
print(type(12.10), type(-21.4), type(0.0))
